    /**
     * 移除 emoji 表情符
     */
    public static String removeEmoji(String txt) {
        if (txt == null || txt.isEmpty()) {
            return txt;
        }
        List<String> eList;
        StringBuilder builder = new StringBuilder();
        char[] chars = txt.toCharArray();

        mainLoop:
        for (int i = 0; i < chars.length; i++) {
            char c = chars[i];
            if ((eList = emojiPrefixMap.get(c)) == null) {
                builder.append(c);
                continue;
            }
            subLoop:
            for (String s : eList) {
                switch (s.length()) {
                    case 1:
                        continue mainLoop;
                    case 2:
                        if (s.charAt(1) == chars[i + 1]) {
                            i += 1;
                            continue mainLoop;
                        }
                        continue subLoop;
                    case 3:
                        if (s.charAt(1) == chars[i + 1] && s.charAt(2) == chars[i + 2]) {
                            i += 2;
                            continue mainLoop;
                        }
                        continue subLoop;
                    case 4:
                        if (s.charAt(1) == chars[i + 1] && s.charAt(2) == chars[i + 2] && s.charAt(3) == chars[i + 3]) {
                            i += 3;
                            continue mainLoop;
                        }
                        continue subLoop;
                    default:
                        break;
                }
            }
            builder.append(c);
        }
        return builder.toString();
    }

